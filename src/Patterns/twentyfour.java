package Patterns;

public class twentyfour {
    public static void main(String[] args) {
        int size=5;
        for(int i=0;i<=i;i++){
           for(int j= 1;j<=size;j++){
            if(i==j || i==size-1 || j==i || j==size-1){
                System.out.print("*");
            }else{
                System.out.print(" ");

           }
           System.out.println();
        }
        }
    }
}
